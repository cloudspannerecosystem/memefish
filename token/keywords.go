var KeywordsMap map[TokenKind]struct{}

func init() {
	KeywordsMap = make(map[TokenKind]struct{})
	for _, k := range Keywords {
		KeywordsMap[k] = struct{}{}
	}
}

func IsKeyword(s string) bool {
	_, ok := KeywordsMap[TokenKind(char.ToUpper(s))]
	return ok
}
